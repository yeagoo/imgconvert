import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const checkMode = process.argv.includes("--check");

function run(command, args, options = {}) {
  return execFileSync(command, args, {
    cwd: options.cwd ?? root,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "inherit"],
  });
}

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

function formatLicense(value, fallback) {
  if (typeof value === "string") return value;
  if (Array.isArray(value)) {
    return value.map((item) => formatLicense(item, fallback)).join(" OR ");
  }
  if (value && typeof value === "object") {
    return [value.type, value.url].filter(Boolean).join(" ");
  }
  return fallback;
}

function findLicenseFiles(packagePath) {
  return readdirSync(packagePath, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => /^(?:license|licence|copying|notice|unlicense)(?:[._-].*)?$/i.test(name))
    .sort((a, b) => a.localeCompare(b));
}

function normalizeLicenseText(text) {
  return text
    .replace(/\r\n?/g, "\n")
    .split("\n")
    .map((line) => line.trimEnd())
    .join("\n")
    .trim();
}

function renderNpmLicenses() {
  const licenseGroups = JSON.parse(run("pnpm", ["licenses", "list", "--json"], { cwd: root }));
  const packages = [];

  for (const [detectedLicense, entries] of Object.entries(licenseGroups)) {
    for (const entry of entries) {
      for (const packagePath of entry.paths ?? []) {
        const packageJsonPath = resolve(packagePath, "package.json");
        const packageJson = existsSync(packageJsonPath) ? readJson(packageJsonPath) : {};
        const name = packageJson.name ?? entry.name;
        const version =
          packageJson.version ??
          (entry.versions?.length === 1 ? entry.versions[0] : entry.versions?.join(", ")) ??
          "unknown";
        const license = formatLicense(packageJson.license, entry.license ?? detectedLicense);
        const homepage =
          packageJson.homepage ?? entry.homepage ?? packageJson.repository?.url ?? "";
        const licenseFiles = findLicenseFiles(packagePath).map((fileName) => ({
          name: fileName,
          text: normalizeLicenseText(readFileSync(resolve(packagePath, fileName), "utf8")),
        }));

        packages.push({
          id: `${name}@${version}`,
          license,
          homepage,
          path: relative(root, packagePath),
          licenseFiles,
        });
      }
    }
  }

  packages.sort((a, b) => a.id.localeCompare(b.id));

  const overview = packages
    .map((pkg) => `- ${pkg.id} — ${pkg.license}${pkg.homepage ? ` — ${pkg.homepage}` : ""}`)
    .join("\n");
  const texts = packages
    .map((pkg) => {
      const files =
        pkg.licenseFiles.length > 0
          ? pkg.licenseFiles
              .map(
                (file) => `#### ${file.name}

\`\`\`text
${file.text}
\`\`\``,
              )
              .join("\n\n")
          : "_No LICENSE/NOTICE file found in the installed package; see package metadata above._";

      return `### ${pkg.id}

- License: ${pkg.license}
- Package path: \`${pkg.path}\`${pkg.homepage ? `\n- Homepage: ${pkg.homepage}` : ""}

${files}`;
    })
    .join("\n\n");

  return `# npm Third-Party Licenses

Generated from \`pnpm licenses list --json\` and installed package license files.

## npm Overview

${overview}

## npm License Texts

${texts}`;
}

const rustLicenses = run(
  "cargo",
  ["about", "generate", "about.hbs", "--manifest-path", "Cargo.toml", "--locked", "--fail"],
  { cwd: resolve(root, "src-tauri") },
);
const npmLicenses = renderNpmLicenses();

const generated = `# THIRD_PARTY_LICENSES

This file is generated by \`pnpm run license:third-party\`.

It is bundled into the application so binary users can inspect open-source
license attributions without opening the source repository.

## Project Notice

ImgConvert
Copyright 2026 The ImgConvert Authors

ImgConvert is licensed under the Apache License, Version 2.0. See \`LICENSE\`
and \`NOTICE\` in this repository for the project license and notice.

${rustLicenses.trim()}

${npmLicenses.trim()}
`;

const outputPaths = [
  resolve(root, "THIRD_PARTY_LICENSES.md"),
  resolve(root, "public", "THIRD_PARTY_LICENSES.md"),
];

if (checkMode) {
  const stale = outputPaths.filter(
    (path) => !existsSync(path) || readFileSync(path, "utf8") !== generated,
  );
  if (stale.length > 0) {
    console.error("Third-party license files are stale:");
    for (const path of stale) {
      console.error(`- ${relative(root, path)}`);
    }
    console.error("Run `pnpm run license:third-party` and commit the generated files.");
    process.exit(1);
  }
  console.log("Third-party license files are up to date.");
} else {
  mkdirSync(resolve(root, "public"), { recursive: true });
  for (const path of outputPaths) {
    writeFileSync(path, generated);
  }
  console.log("Generated THIRD_PARTY_LICENSES.md and public/THIRD_PARTY_LICENSES.md");
}
